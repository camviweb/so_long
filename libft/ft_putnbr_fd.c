#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	c;
	char	sign;

	sign = '-';
	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
	}
	else if (n < 0)
	{
		write(fd, &sign, 1);
		n = -n;
		ft_putnbr_fd(n, fd);
	}
	else if (n < 10)
	{	
		c = n + '0';
		write(fd, &c, 1);
	}
	else if (n > 9)
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
	}
}
